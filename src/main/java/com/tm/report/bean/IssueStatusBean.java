package com.tm.report.bean;

public class IssueStatusBean {
	
	private String repIssueStatus;
	private int repNumIssues;
	
	public String getRepIssueStatus() {
		return repIssueStatus;
	}
	public void setRepIssueStatus(final String repIssueStatus) {
		this.repIssueStatus = repIssueStatus;
	}
	public int getRepNumIssues() {
		return repNumIssues;
	}
	public void setRepNumIssues(final int repNumIssues) {
		this.repNumIssues = repNumIssues;
	}
}
